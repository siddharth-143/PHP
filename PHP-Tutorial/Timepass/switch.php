<?php
    $a = "Sun";

    switch($a)
    {
        case "Mon":
            echo "This is monday";
            break;
        case "Tue":
            echo "This is tuesday";
            break;
        case "Wed":
            echo "This is wednesday";
            break;
        case "Thu":
            echo "This is thursday";
            break;
        case "Fri":
            echo "This is friday";
            break;
        case "Sat":
            echo "This is saturday";
            break;
        case "Sun":
            echo " This is sunday";
            break;
    }

    # Switch statement :
        /* If you want to select one of the many blocks of code to be executed,
        use the switch statement.

        The switch statement is used to avoid long blocks of if.....elseif......else code.
        */
?>